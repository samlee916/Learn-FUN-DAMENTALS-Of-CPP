#include <iostream>

using namespace std;

//data types
int main() {

    cout << "Size of char is " << sizeof(char) << " byte " << endl;
    cout << "Size of int is " << sizeof(int) << " byte " << endl;
    cout << "Size of floating point is " << sizeof(float) << " byte " << endl;
    cout << "Size of double is " << sizeof(double) << " byte " << endl;
    cout << "Size of bool is " << sizeof(bool) << " byte " << endl;
    cout << "Size of whar_t is " << sizeof(wchar_t) << " byte " << endl;
    cout << "Size of signed int is " << sizeof(signed int) << " byte " << endl;
    cout << "Size of long int is " << sizeof(long int) << " byte " << endl;
    cout << "Size of unsigned long int is " << sizeof(unsigned long int) << " byte " << endl;

    return 0;

}
